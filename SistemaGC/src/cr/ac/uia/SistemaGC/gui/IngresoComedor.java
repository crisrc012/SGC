    private void aplicarIngreso() {
        if (txtCedPersona.getText().isEmpty()) {
            JOptionPane.showOptionDialog(this,
                    "Por favor digite una cédula válida",
                    "Error",
                    JOptionPane.OK_OPTION,
                    JOptionPane.ERROR_MESSAGE,
                    null,
                    new Object[]{"Entendido"},
                    "Entendido");
            return;
        }
        if (!rbtnAlmuerzo.isSelected() && !rbtnDesayuno.isSelected()) {
            JOptionPane.showOptionDialog(this,
                    "Por favor elija una comida",
                    "Error",
                    JOptionPane.OK_OPTION,
                    JOptionPane.ERROR_MESSAGE,
                    null,
                    new Object[]{"Entendido"},
                    "Entendido");
            return;
        }
        try {
            int cedula = Integer.parseInt(txtCedPersona.getText());
            Boolean becado = ConfirmarBeca(cedula);
            if (becado == true) {
                int comida = 0;
                String tipo = "";
                if (rbtnDesayuno.isSelected()) {
                    comida = 1;
                    tipo = "desayuno";
                } else if (rbtnAlmuerzo.isSelected()) {
                    comida = 2;
                    tipo = "almuerzo";
                }
                Tiquetes_bl tbl = new Tiquetes_bl();
                int cantidad = tbl.count(cedula, comida);
                if (cantidad > 1) {
                    if (JOptionPane.showOptionDialog(this,
                            "Usted tiene disponibles: " + cantidad + " tiquetes de " + tipo + ".\n"
                            + "¿Desea utilizar uno?",
                            "Confirmar",
                            JOptionPane.YES_NO_OPTION,
                            JOptionPane.WARNING_MESSAGE,
                            null,
                            new Object[]{"Sí, usar", "No, no usar"},
                            "No, no usar") == JOptionPane.YES_OPTION) {
                        ArrayList<Tiquetes> al = tbl.activos(cedula, comida);
                        Tiquetes t = al.get(0);
                        t.setActivo(false); // Desactivando tiquete
                        if (tbl.update(t)) {
                            JOptionPane.showMessageDialog(this,
                                    "Se ha usado correctamente el tiquete",
                                    "Correcto", JOptionPane.INFORMATION_MESSAGE);
                            this.limpiar();
                        } else {
                            JOptionPane.showMessageDialog(this,
                                    "Ha ocurrido un error por favor intentelo de nuevo",
                                    "Correcto", JOptionPane.INFORMATION_MESSAGE);
                            this.dispose();
                        }
                    } else {
                        this.limpiar();
                    }
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Por favor compre un tiquete",
                            "Error", JOptionPane.ERROR_MESSAGE);
                    this.limpiar();
                }
            }
        } catch (IOException | SQLException e) {
            System.out.println(e.toString());
        }
    }

