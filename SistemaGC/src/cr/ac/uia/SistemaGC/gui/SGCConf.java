    private void error() {
        JOptionPane.showMessageDialog(this, "Error al actualizar, por favor verifique los datos",
                "Error", JOptionPane.INFORMATION_MESSAGE);
    }

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try (
                OutputStream output
                = new FileOutputStream("db.properties")) {
            prop.setProperty("Servidor", txtServidor.getText());
            prop.setProperty("Puerto", txtPuerto.getValue().toString());
            prop.setProperty("BD", txtBasedeDatos.getText());
            prop.setProperty("Usuario", txtUsuario.getText());
            String contrasena = "";
            for (char a : txtContrasena.getPassword()) {
                contrasena += a;
            }
            prop.setProperty("Clave", contrasena);
            prop.store(output, null);
            JOptionPane.showMessageDialog(this, "Configuración actualizada correctamente",
                    "Correcto", JOptionPane.INFORMATION_MESSAGE);
        } catch (FileNotFoundException e) {
            error();
            System.out.println(e.toString());
        } catch (IOException e) {
            error();
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

