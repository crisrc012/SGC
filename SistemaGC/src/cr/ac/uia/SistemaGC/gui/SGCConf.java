    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try {
            // Cargando archivo de confgiuracion
            prop.load(new FileInputStream("db.properties"));
            txtServidor.setText(prop.getProperty("Servidor"));
            txtBasedeDatos.setText(prop.getProperty("BD"));
            txtUsuario.setText(prop.getProperty("Usuario"));
            txtContrasena.setText(prop.getProperty("Clave"));
            txtPuerto.setValue(Integer.parseInt(prop.getProperty("Puerto")));
        } catch (FileNotFoundException e) {
            System.out.println(e.toString());
        } catch (IOException e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_formComponentShown

